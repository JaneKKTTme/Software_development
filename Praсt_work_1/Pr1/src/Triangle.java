    public static boolean isNumeric(String str)  
    {  
        try {  
            double d = Double.parseDouble(str);  
        }  
        catch(NumberFormatException nfe) {  
            return false;  
        }  
        return true;  
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (isNumeric(jTextField1.getText()) && isNumeric(jTextField2.getText()) 
                && isNumeric(jTextField3.getText())) {
            double a = Double.parseDouble(jTextField1.getText());
            double b = Double.parseDouble(jTextField2.getText());
            double c = Double.parseDouble(jTextField3.getText());
            if (a > 0 && b > 0 && c > 0) {
                double p = (a+b+c)/2;
            jLabel5.setText(Double.toString(Math.sqrt(p*(p-a)*(p-b)*(p-c))));
            }
            else 
                jLabel5.setText("Such triangle is not exists!");
        }
        else {
            jLabel5.setText("Such triangle is not exists!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

