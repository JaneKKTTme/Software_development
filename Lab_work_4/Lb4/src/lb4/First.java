    public int[] inputSort(int n, int[] a) {
        int tmp, item;
        for (int i = 1; i < n; i++) {
            tmp = a[i]; 
            item = i-1; 
            while(item >= 0 && a[item] < tmp) {
            a[item + 1] = a[item]; 
            a[item] = tmp;
            item--;
            }
        }
        return a;
    }
    
